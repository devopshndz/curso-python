# conversion de tipos
# int() -> convierte a entero
# str() -> convierte a string
# float() -> convierte a float
# bool() -> convierte a boolean, tener mucho cuidado con esta función (0, "", None) van a dar flase, el resto dará true

print(bool(""))
print(bool("0"))
print(bool(None))
print(bool(" "))
print(bool(0))
